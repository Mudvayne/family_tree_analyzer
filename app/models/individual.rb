class Individual
  def initialize(id, name, dateofbirth, dateofmarriage, dateofdeath, dateofburial, placeofbirth, placeofmarriage, placeofdeath, placeofburial)
    @id = id
    @name = name
    @dateofbirth = dateofbirth
    @dateofmarriage = dateofmarriage
    @dateofdeath = dateofdeath
    @dateofburial = dateofburial
    @placeofbirth = placeofbirth
    @placeofmarriage = placeofmarriage
    @placeofdeath = placeofdeath
    @placeofburial = placeofburial
  end

  def id
    @id
  end

  def name
    @name
  end

  def dateofbirth
    @dateofbirth
  end

  def dateofmarriage
    @dateofmarriage
  end

  def dateofdeath
    @dateofdeath
  end

  def dateofburial
    @dateofburial
  end

  def placeofbirth
    @placeofbirth
  end

  def placeofmarriage
    @placeofmarriage
  end

  def placeofdeath
    @placeofdeath
  end

  def placeofburial
    @placeofburial
  end
end